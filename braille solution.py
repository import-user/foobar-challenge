braillecodes = [
'000000',
'100000',
'110000',
'100100',
'100110',
'100010',
'110100',
'110110',
'110010',
'010100',
'010110',
'101000',
'111000',
'101100',
'101110',
'101010',
'111100',
'111110',
'111010',
'011100',
'011110',
'101001',
'111001',
'010111',
'101101',
'101111',
'101011']

def solution(s):
    ans = ""
    for x in s:
        if x==' ':
            ans += "000000"
        else:
            if x >= 'A' and x <= 'Z':
                ans += "000001"
                ans += braillecodes[ord(x) - 96 + 32]
            else:
                ans += braillecodes[ord(x) - 96]
    return ans
